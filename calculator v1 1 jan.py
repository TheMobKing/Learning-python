def add(_first,_second):
    return _first + _second
def multiply(_first,_second):
    return _first * _second
def divide(_first,_second):
    return _first / _second
def minize(_first,_second):
    return _first - _second

print(add(33,33))
print(multiply(33,33))
print(divide(33,3))
print(minize(33,33))
